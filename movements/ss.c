#include "../push_swap.h"

void	ss(t_list **sta, t_list **stb)
{
	if (*sta == NULL || (*sta)->next == NULL)
		return ;
	if (*stb == NULL || (*stb)->next == NULL)
		return ;
	sa(sta, 'x');
	sb(stb, 'x');
	write(1, "ss", 2);
}
