#include "../push_swap.h"

void	rr(t_list **sta, t_list **stb)
{
	if ((*sta)->next == NULL || (*stb)->next == NULL)
		return ;
	ra(sta, 'x');
	rb(stb, 'x');
	write(1, "rr", 2);
}
